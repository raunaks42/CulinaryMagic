					<h2 class="procedures-h">How?</h2>
					<div class="procedure"><span class="list-number">1.</span> Place the pressure pan on flame. Add some ghee and oil together. Cooking it in full ghee would make this extremely rich. Though great for a party, we would not want this on a regular basis.</div>
					<div class="procedure"><span class="list-number">2.</span> Marinade the chicken in curd, dhaniya, chilli, turmeric, garam masala and salt.</div>
					<div class="procedure"><span class="list-number">3.</span> In the meanwhile, prep for onion and ginger garlic paste in a mixer Jar. Separately, in a large bowl blanch the spinach leaves for about 5 mins. Run this under cold water to retain the dark green color. Thinly shred the methi leaves. This need not be boiled as it may lend a bitter taste in the mouth.</div>
					<div class="procedure"><span class="list-number">4.</span> When the oil is medium hot, throw in some whole garam masala (cardamom, cloves, cinnamon, and bay leaves to flavour the oil.</div>
					<div class="procedure"><span class="list-number">5.</span> Add onion paste and fry till it is a little pink in color. Add ginger garlic paste and fry for some more time till the oil separates on the sides of the pan.Add chopped tomatoes and methi leaves to the masala.</div>
					<div class="procedure"><span class="list-number">6.</span> Now its time to add the marinated chicken. Make sure that the flame is high at this point as its important to seal the juices in the chicken. Fry all for about 5 to 7 mins.</div>
					<div class="procedure"><span class="list-number">7.</span> Lower the flame to medium and let everything simmer for about 10 to 12 mins more. Make sure that you keep stirring in between.</div>
					<div class="procedure"><span class="list-number">8.</span> Once you see everything is done to a large extent, add the spinach paste to it.</div>
					<div class="procedure"><span class="list-number">9.</span> Check the consistency of the gravy and if you find too dry, add a little water just enough to let the chicken pressure cook upto 2 whistles.</div>
					<div class="procedure"><span class="list-number">10.</span> Let it cool for a bit before the super sumptuous chicken saagwala is ready to be digged in !</div>
